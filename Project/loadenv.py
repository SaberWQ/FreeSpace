# -*- coding: utf-8 -*-
"""
Модуль для завантаження змінних оточення
Читає .env файл та встановлює змінні середовища
"""

# Імпорт модуля для роботи з операційною системою
import os
# Імпорт модуля для роботи з .env файлами
from dotenv import load_dotenv

def load_environment():
    """
    Завантажує змінні оточення з .env файлу
    Викликається при старті додатку
    """
    # Шукаємо .env файл у поточній директорії
    dotenv_path = os.path.join(os.path.dirname(__file__), '..', '.env')
    
    # Якщо файл існує - завантажуємо змінні
    if os.path.exists(dotenv_path):
        # Завантажуємо змінні з файлу
        load_dotenv(dotenv_path)
        # Виводимо повідомлення про успішне завантаження
        print("✓ Змінні оточення завантажено з .env файлу")
    else:
        # Виводимо попередження якщо файл не знайдено
        print("⚠ Файл .env не знайдено, використовуються значення за замовчуванням")

def get_env_variable(var_name, default=None):
    """
    Отримує значення змінної оточення
    
    Параметри:
        var_name (str): Назва змінної
        default: Значення за замовчуванням
        
    Повертає:
        str: Значення змінної або default
    """
    # Отримуємо значення змінної з оточення
    value = os.environ.get(var_name, default)
    # Повертаємо значення
    return value
